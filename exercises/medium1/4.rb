require 'pry'

class CircularQueue
  attr_accessor :contents
  attr_reader :max_size

  def initialize(size)
    @max_size = size
    @contents = []
  end

  def enqueue(obj)
    contents.shift if full?
    contents.push(obj)
  end

  def dequeue
    contents.shift
  end

  def full?
    contents.size == max_size
  end

end

queue = CircularQueue.new(3)
puts queue.dequeue == nil

queue.enqueue(1)
queue.enqueue(2)
puts queue.dequeue == 1

queue.enqueue(3)
queue.enqueue(4)
puts queue.dequeue == 2

queue.enqueue(5)
queue.enqueue(6)
queue.enqueue(7)
puts queue.dequeue == 5
puts queue.dequeue == 6
puts queue.dequeue == 7
puts queue.dequeue == nil

